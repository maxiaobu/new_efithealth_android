package com.efithealth.app.javabean;

public class MemberMolder {
	private String msgFlag;
	private String msgContent;
	private Member member;
	private String shopcount;
	private String ordercount;
	private String lessoncount;
	private String ycoinnum;
	
	
	public String getMsgFlag() {
		return msgFlag;
	}
	public void setMsgFlag(String msgFlag) {
		this.msgFlag = msgFlag;
	}
	public String getMsgContent() {
		return msgContent;
	}
	public void setMsgContent(String msgContent) {
		this.msgContent = msgContent;
	}
	public Member getMember() {
		return member;
	}
	public void setMember(Member member) {
		this.member = member;
	}
	public String getShopcount() {
		return shopcount;
	}
	public void setShopcount(String shopcount) {
		this.shopcount = shopcount;
	}
	public String getOrdercount() {
		return ordercount;
	}
	public void setOrdercount(String ordercount) {
		this.ordercount = ordercount;
	}
	public String getLessoncount() {
		return lessoncount;
	}
	public void setLessoncount(String lessoncount) {
		this.lessoncount = lessoncount;
	}
	public String getYcoinnum() {
		return ycoinnum;
	}
	public void setYcoinnum(String ycoinnum) {
		this.ycoinnum = ycoinnum;
	}
	
	
}
